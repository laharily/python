# Relational operators are also called conditional operators and allow you to
# write boolean expressions.
x = 10
print (x > 10)
print (x >= 10)

# >, <, !=, ==, >=, <=
y = 6
# logical operators allow you to combine more than one boolean expression
# and, or, not
print (x > 5 and y > 5)
print (not y > 5)

